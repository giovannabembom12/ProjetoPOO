package model.vo;

public class EventoVO {
	private int id_event;
	private String nome_e, desc_ev, dt_event, local;
	
	public int getId_event() {
		return id_event;
	}
	public void setId_event(int id_event) {
		this.id_event = id_event;
	}
	public String getNome_e() {
		return nome_e;
	}
	public void setNome_e(String nome_e) {
		this.nome_e = nome_e;
	}
	public String getDesc_ev() {
		return desc_ev;
	}
	public void setDesc_ev(String desc_ev) {
		this.desc_ev = desc_ev;
	}
	public String getDt_event() {
		return dt_event;
	}
	public void setDt_event(String dt_event) {
		this.dt_event = dt_event;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	
	
	
}
